/// Verify that trade records written by v1 are readable after upgrade.
#[test]
fn test_trade_record_preserved_after_upgrade() {
    let (env, contract_id, _admin) = setup_v1();
    let user = Address::generate(&env);
    let signal_id: u64 = 7;

    seed_signal(&env, &contract_id, signal_id);

    // --- v1: authorize user and execute a trade via the client ---
    env.as_contract(&contract_id, || {
        authorize_user_with_limits(&env, &user, 1_000_000_000, 30);
    });

    let v1_client = AutoTradeContractClient::new(&env, &contract_id);
    // Trade may succeed or fail (balance stub); either way a record is written.
    let _ = v1_client.try_execute_trade(&user, &signal_id, &OrderType::Market, &TRADE_AMOUNT);

    // --- simulate upgrade ---
    env.register_at(&contract_id, AutoTradeContractV2, ());

    // --- v2: trade record must still be readable ---
    env.as_contract(&contract_id, || {
        use auto_trade::storage::DataKey;
        let stored = env
            .storage()
            .persistent()
            .get::<_, auto_trade::Trade>(&DataKey::Trades(user.clone(), signal_id));
        // If the trade was recorded, verify the fields are correct.
        if let Some(trade) = stored {
            assert_eq!(trade.signal_id, signal_id);
            assert_eq!(trade.user, user);
        }
    });
}

/// Verify that the new v2 function works correctly after upgrade.
#[test]
fn test_v2_new_function_works() {
    let (env, contract_id, _admin) = setup_v1();

    // --- simulate upgrade ---
    let v2_id = env.register_at(&contract_id, AutoTradeContractV2, ());
    let client = AutoTradeContractV2Client::new(&env, &v2_id);

    let version = client.get_contract_version();
    assert_eq!(version, String::from_str(&env, "v2.0.0"));
}

/// Verify that multiple pieces of state are all preserved simultaneously.
#[test]
fn test_all_v1_state_accessible_in_v2() {
    let (env, contract_id, _admin) = setup_v1();
    let user = Address::generate(&env);

    // Seed signal
    seed_signal(&env, &contract_id, 1);

    // Authorize user
    env.as_contract(&contract_id, || {
        authorize_user_with_limits(&env, &user, 5_000_000_000, 60);
    });

    // Open position
    let trade_id: BytesN<32> = env.as_contract(&contract_id, || {
        auto_trade::positions::open_position(&env, &user, 1, 1, 1000, 100, 80, 130)
    });

    // --- simulate upgrade ---
    let v2_id = env.register_at(&contract_id, AutoTradeContractV2, ());
    let client = AutoTradeContractV2Client::new(&env, &v2_id);

    // Signal preserved
    assert!(client.get_signal_v2(&1u64).is_some());

    // Auth preserved
    let cfg = client.get_auth_config_v2(&user).unwrap();
    assert!(cfg.authorized);
    assert_eq!(cfg.max_trade_amount, 5_000_000_000);

    // Position preserved
    env.as_contract(&contract_id, || {
        let pos = auto_trade::positions::get_position(&env, &trade_id).unwrap();
        assert_eq!(pos.amount, 1000);
    });

    // v2 function works
    assert_eq!(
        client.get_contract_version(),
        String::from_str(&env, "v2.0.0")
    );
}

/// Verify that upgrade is only callable by admin.
///
/// In Soroban, the `upgrade` host function requires the contract itself to
/// authorize the call (i.e., the admin must invoke it through the contract).
/// We test this by verifying that `require_admin` rejects non-admin callers
/// when they attempt to invoke admin-gated functions post-upgrade.
#[test]
fn test_upgrade_only_callable_by_admin() {
    let (env, contract_id, admin) = setup_v1();
    let non_admin = Address::generate(&env);

    let v1_client = AutoTradeContractClient::new(&env, &contract_id);

    // Non-admin cannot call admin-gated functions (e.g., pause_category).
    let result = v1_client.try_pause_category(
        &non_admin,
        &String::from_str(&env, "trading"),
        &None,
        &String::from_str(&env, "test"),
    );
    assert!(
        result.is_err(),
        "non-admin must not be able to call admin functions"
    );

    // Admin can call admin-gated functions.
    let result = v1_client.try_pause_category(
        &admin,
        &String::from_str(&env, "trading"),
        &None,
        &String::from_str(&env, "admin pause"),
    );
    assert!(result.is_ok(), "admin must be able to call admin functions");
}

/// Verify that migrated v1 state is correct (values match what was written).
#[test]
fn test_migrated_v1_state_is_correct() {
    let (env, contract_id, _admin) = setup_v1();
    let user = Address::generate(&env);

    // Write known values in v1
    env.as_contract(&contract_id, || {
        set_signal(
            &env,
            99,
            &Signal {
                signal_id: 99,
                price: 42_000,
                expiry: env.ledger().timestamp() + SIGNAL_EXPIRY_OFFSET,
                base_asset: 7,
            },
        );
        authorize_user_with_limits(&env, &user, 999_999, 10);
    });

    // Upgrade
    let v2_id = env.register_at(&contract_id, AutoTradeContractV2, ());
    let client = AutoTradeContractV2Client::new(&env, &v2_id);

    // Verify exact values
    let sig = client.get_signal_v2(&99u64).unwrap();
    assert_eq!(sig.price, 42_000);
    assert_eq!(sig.base_asset, 7);

    let cfg = client.get_auth_config_v2(&user).unwrap();
    assert_eq!(cfg.max_trade_amount, 999_999);
}
